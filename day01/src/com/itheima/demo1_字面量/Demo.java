package com.itheima.demo1_字面量;

public class Demo {
    public static void main(String[] args) {
        //打印各种类型的字面量
        System.out.println(10);//输出整数
        System.out.println(12.2);//输出小数
        //输出字符  字符用单引号引起来，里面有且仅有一个字符
        System.out.println('A');
        //输出字符串
        System.out.println("System");
    }
}
