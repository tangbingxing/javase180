package com.itheima.homemorks;
//使用十进制,二进制,八进制,十六进制表示整数98
public class Demo1 {
    public static void main(String[] args) {
        System.out.println("十进制表示" + 98);
        System.out.println("二进制表示" + 0B01100010);
        System.out.println("八进制表示" + 0142);
        System.out.println("十六进制表示" + 0X62);
    }
}
